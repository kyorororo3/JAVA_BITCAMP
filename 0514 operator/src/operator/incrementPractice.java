package operator;

public class incrementPractice {

	public static void main(String[] args) {
		
		int num3 = 0;
		int num4;
		
		
		num4 =num3++;
		System.out.println("num3 = "+num3);
		System.out.println("num4 = "+num4);
		
		System.out.println("--------------------");
		
		num4 = ++num3;
		System.out.println("num3 = "+ num3);
		System.out.println("num4 =" + num4);
	 	

	}

}
